package Step4;

public class lesson8 {

    public static void main(String[] args) {
        // 1から10まで表示(順で)
        System.out.println("1から10まで:");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // 10から1まで表示(逆順で)
        System.out.println("10から1まで:");
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}