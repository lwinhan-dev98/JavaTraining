package Step4;

public class lesson2 {
    public static void main(String[] args) {
        // 2つの整数型変数を宣言して代入
        int num1 = 10;
        int num2 = 20;

        // 合計を計算して表示
        int sum = num1 + num2;
        System.out.println(sum);

        // 差を計算して表示（大きい方から小さい方）
        int diff = num2 - num1; 
        System.out.println(diff);

        // 積を計算して表示
        int product = num1 * num2;
        System.out.println(product);
    }
}